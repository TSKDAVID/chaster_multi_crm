-- Chaster: tenants and platform tables (CRM rows are scoped by tenant_id)
create table public.tenants (
    id uuid primary key default gen_random_uuid(),
    company_name text not null,
    slug text not null unique,
    status text not null default 'trial',
    subscription_tier text not null default 'starter',
    trial_ends_at timestamptz,
    created_at timestamptz not null default now(),
    owner_user_id uuid references auth.users (id),
    notes text
);

create table public.tenant_members (
    id uuid primary key default gen_random_uuid(),
    tenant_id uuid not null references public.tenants (id) on delete cascade,
    user_id uuid not null references auth.users (id) on delete cascade,
    role text not null default 'member',
    invited_by uuid references auth.users (id),
    joined_at timestamptz not null default now(),
    unique (tenant_id, user_id)
);

create index tenant_members_user_id_idx on public.tenant_members using btree (user_id);
create index tenant_members_tenant_id_idx on public.tenant_members using btree (tenant_id);

create table public.chaster_team (
    id uuid primary key default gen_random_uuid(),
    user_id uuid not null unique references auth.users (id) on delete cascade,
    role text not null default 'staff',
    added_at timestamptz not null default now()
);

create table public.knowledge_base_documents (
    id uuid primary key default gen_random_uuid(),
    tenant_id uuid not null references public.tenants (id) on delete cascade,
    file_name text not null,
    file_type text not null,
    storage_path text,
    status text not null default 'processing',
    uploaded_by uuid references auth.users (id),
    uploaded_at timestamptz not null default now(),
    file_size_bytes bigint,
    content_json jsonb
);

create index knowledge_base_documents_tenant_id_idx
    on public.knowledge_base_documents using btree (tenant_id);

create table public.audit_logs (
    id uuid primary key default gen_random_uuid(),
    tenant_id uuid references public.tenants (id) on delete set null,
    actor_user_id uuid references auth.users (id),
    action text not null,
    target_user_id uuid references auth.users (id),
    metadata jsonb not null default '{}'::jsonb,
    created_at timestamptz not null default now()
);

create index audit_logs_tenant_id_idx on public.audit_logs using btree (tenant_id);
create index audit_logs_created_at_idx on public.audit_logs using btree (created_at desc);

create table public.tenant_invites (
    id uuid primary key default gen_random_uuid(),
    tenant_id uuid not null references public.tenants (id) on delete cascade,
    email text not null,
    role text not null check (role in ('member', 'admin')),
    invited_by uuid references auth.users (id),
    auth_user_id uuid,
    created_at timestamptz not null default now(),
    accepted_at timestamptz,
    cancelled_at timestamptz,
    unique (tenant_id, email)
);

create index tenant_invites_tenant_pending_idx
    on public.tenant_invites (tenant_id)
    where accepted_at is null and cancelled_at is null;

create table public.tenant_settings (
    id uuid primary key default gen_random_uuid(),
    tenant_id uuid not null unique references public.tenants (id) on delete cascade,
    ai_tone text not null default 'professional',
    escalation_threshold double precision not null default 0.6,
    business_hours_start time not null default '09:00',
    business_hours_end time not null default '17:00',
    timezone text not null default 'UTC',
    widget_primary_color text not null default '#6366f1',
    widget_welcome_message text not null default 'Hi! How can I help you today?',
    widget_position text not null default 'bottom-right',
    crm_module_enabled boolean not null default true,
    widget_module_enabled boolean not null default true
);

create table public.companies (
    id bigint generated by default as identity primary key,
    created_at timestamp with time zone not null default now(),
    name text not null,
    sector text,
    size smallint,
    linkedin_url text,
    website text,
    phone_number text,
    address text,
    zipcode text,
    city text,
    state_abbr text,
    sales_id bigint,
    context_links json,
    country text,
    description text,
    revenue text,
    tax_identifier text,
    logo jsonb,
    tenant_id uuid not null references public.tenants (id)
);

create table public.contacts (
    id bigint generated by default as identity primary key,
    first_name text,
    last_name text,
    gender text,
    title text,
    background text,
    avatar jsonb,
    first_seen timestamp with time zone,
    last_seen timestamp with time zone,
    has_newsletter boolean,
    status text,
    tags bigint[],
    company_id bigint,
    sales_id bigint,
    linkedin_url text,
    email_jsonb jsonb,
    phone_jsonb jsonb,
    tenant_id uuid not null references public.tenants (id)
);

create table public.contact_notes (
    id bigint generated by default as identity (sequence name public."contactNotes_id_seq") not null,
    contact_id bigint not null,
    text text,
    date timestamp with time zone default now(),
    sales_id bigint,
    status text,
    attachments jsonb[],
    tenant_id uuid not null references public.tenants (id)
);

create table public.deals (
    id bigint generated by default as identity primary key,
    name text not null,
    company_id bigint,
    contact_ids bigint[],
    category text,
    stage text not null,
    description text,
    amount bigint,
    created_at timestamp with time zone not null default now(),
    updated_at timestamp with time zone not null default now(),
    archived_at timestamp with time zone,
    expected_closing_date date,
    sales_id bigint,
    index smallint,
    tenant_id uuid not null references public.tenants (id)
);

create table public.deal_notes (
    id bigint generated by default as identity (sequence name public."dealNotes_id_seq") not null,
    deal_id bigint not null,
    type text,
    text text,
    date timestamp with time zone default now(),
    sales_id bigint,
    attachments jsonb[],
    tenant_id uuid not null references public.tenants (id)
);

create table public.sales (
    id bigint generated by default as identity primary key,
    first_name text not null default 'Pending'::text,
    last_name text not null default 'Pending'::text,
    email text not null,
    administrator boolean not null,
    user_id uuid not null,
    avatar jsonb,
    disabled boolean not null default false
);

create unique index uq__sales__user_id on public.sales using btree (user_id);

create table public.tags (
    id bigint generated by default as identity primary key,
    name text not null,
    color text not null,
    tenant_id uuid not null references public.tenants (id)
);

create table public.tasks (
    id bigint generated by default as identity primary key,
    contact_id bigint not null,
    type text,
    text text,
    due_date timestamp with time zone,
    done_date timestamp with time zone,
    sales_id bigint,
    tenant_id uuid not null references public.tenants (id)
);

create table public.configuration (
    id integer not null default 1 primary key,
    config jsonb not null default '{}'::jsonb,
    constraint configuration_singleton check (id = 1)
);

create table public.favicons_excluded_domains (
    id bigint generated by default as identity primary key,
